/**
 * Erzeugt eine Zufallszahl im angegebenen Bereich
 * @param {number} min
 * @param {number} max
 * @returns {number} Eine Zufallszahl zwischen min und max.
 */
const cryptoRandomInt = (min, max) => {
  const range = max - min;
  if (range <= 0) {
    throw new Error('Invalid range');
  }
  const maxUint32 = 0xffffffff;
  const buckets = maxUint32 / range;
  const limit = buckets * range;

  let rand;
  do {
    rand = crypto.getRandomValues(new Uint32Array(1))[0];
  } while (rand >= limit);

  return min + (rand % range);
};

/**
 * Gibt ein zufälliges Element aus einem Array zurück
 * @param {Array} array
 * @returns {any} Eint zufälliges Element aus dem angebenen Array.
 */
const getRandomElement = array => {
  const randomIndex = cryptoRandomInt(0, array.length);
  return array[randomIndex];
};

/**
 * Ermittelt die für das angegebene grammatikalishe Geschlecht flektierte Form des angegebenen Adjektivs.
 * @param {string} adjective
 * @param {'f'|'m'|'n'} gender
 * @returns {string} - Das übergebene Adjektiv in der dem grammatikalischen Geschlecht entsprechenden Form.
 */
const getGenderedAdjective = (adjective, gender) => {
  let ending = /**@type{string}*/ (adjectiveEndings.get(gender));
  let processedAdjective = adjective;

  if (adjective.endsWith('e')) {
    ending = ending.replace('e', '');
  } else if (adjective.endsWith('el') || adjective.match(/[aeiou]er$/gm)) {
    processedAdjective = processedAdjective.slice(0, -2) + processedAdjective.slice(-1);
  } else if (irregularAdjectives.has(adjective)) {
    const factory = /**@type {(string)=>string}*/ (irregularAdjectives.get(adjective));
    processedAdjective = factory(gender);
    ending = '';
  }

  return `${processedAdjective}${ending}`;
};

/**
 * Erzeugt eine Passphrase
 * @returns {string} Eine Passphrase nach dem Muster "Adjektiv-Adjektiv-Nomen-Zahl"
 */
export const generatePassphrase = () => {
  const gen = getRandomElement(gender);
  const adjective1 = getGenderedAdjective(getRandomElement(adjectives), gen);
  const adjective2 = getGenderedAdjective(getRandomElement(adjectives), gen);
  const noun = getRandomElement(/** @type {Array<string>} */ (nouns.get(gen)));
  const number = cryptoRandomInt(1, 101);

  return `${adjective1}-${adjective2}-${noun}-${number}`;
};
